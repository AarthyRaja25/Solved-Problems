package org.task;

public class switchwithbreak {
	public static void main(String[] args){
		int a=20;
		switch(a)
		{

		case 10:System.out.print("10");
				break;
		case 20:System.out.print("20");
		break;
		case 30:System.out.print("30");
		break;
				
		default:System.out.print("!10");
				break;
		}
		
	}

}
