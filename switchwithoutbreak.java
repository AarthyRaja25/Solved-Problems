package org.task;

public class switchwithoutbreak {
	
		public static void main(String[] args){
			
			switch(20)
			{

			case 10:
				System.out.println("10");
					
			case 20:
				System.out.println("20");
			
			case 30:
				System.out.println("30");
			
					
			default:
				System.out.println("40");
					
			}
			
		}

	}



